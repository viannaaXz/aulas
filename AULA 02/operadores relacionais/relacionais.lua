-- igualdade (igual) // (==)
-- desigualdade (diferente) // (~=)
-- menor que // (<)
-- maior que // (>)
-- menor ou igual // (<=)
-- maior ou igual // (>=)

local a = 2
local b = 4
local c = 2
local d = 4

-- igual
print(a==b) -- se for igual retorna true, se não for retorna false
print(a==c) -- se for igual retorna true, se não for retorna false

-- diferente
print(a~=b) -- se for diferente retorna true, se não for retorna false
print(a~=c) -- se for diferente retorna true, se não for retorna false

-- menor que
print(a<b) -- se for menor que retorna true, se não for retorna false
print(b<a) -- se for menor que retorna true, se não for retorna false

-- maior que
print(a>b) -- se for maior que retorna true, se não for retorna false
print(b>a) -- se for maior que retorna true, se não for retorna false

-- menor ou igual
print(a<=b) -- se for menor ou igual retorna true, se não for retorna false
print(b<=a) -- se for menorou igual retorna true, se não for retorna false

-- maior ou igual
print(a>=b) -- se for maior ou igual retorna true, se não for retorna false
print(b>=a) -- se for maior ou igual retorna true, se não for retorna false

