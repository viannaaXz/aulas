-- VARIAVEIS
local valorA = 5
local valorB = 2
local valorC = 10
local soma = "soma"
local sub = "subtração"
local multi = "multiplicação"
local div = "divisão"
local divI = "divisão inteira"

-- SOMA
print(soma)
print(valorA+valorB)

-- SUBTRAÇÃO
print(sub)
print(valorA-valorB)

-- MULTIPLICAÇÃO
print(multi)
print(valorC*valorB)

-- DIVISÃO
print(div)
print(valorC/valorB)

-- DIVISÃO INTEIRA
print(divI)
print(valorC//valorB)